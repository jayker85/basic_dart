void main(){

  var warna = "greenu";

  switch(warna){
    case 'red':
      print('Warna merah');
      break;
    case 'orange':
      print('Warna jingga');
      break;
    case 'yellow':
      print('Warna kuning');
      break;
    case 'blue':
      print('Warna biru');
      break;
    case 'green':
      print('Warna hijau');
      break;
    default:
      print('Warna tidak terdeteksi');
  }

}